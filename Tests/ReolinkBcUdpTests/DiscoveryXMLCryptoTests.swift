        let plain = DiscoveryXMLCrypto.decrypt(ciphertext, offset: 0x003D_0B9B)
        let xml = try #require(String(data: plain, encoding: .ascii))
        #expect(xml.hasPrefix("<P2P>"))
        #expect(xml.contains("<C2M_Q>"))
        // Real UID from the capture; sanity-check the cipher
        // round-trip against ground truth.
        #expect(xml.contains("<uid>9527000I500W1NSQ</uid>"))
        // Confirm the platform tag — the wire uses `<p>`, not
        // `<s>` as I mis-transcribed in Phase 3d.2's first
        // pass.
        #expect(xml.contains("<p>MAC</p>"))
    }

    @Test("Encrypt then decrypt with same offset round-trips byte-identically")
    func roundTrips() {
        let original = Data("<P2P><C2M_Q><uid>FEED1234FACE5678</uid></C2M_Q></P2P>".utf8)
        for offset: UInt32 in [0, 1, 0x1234_5678, 0xFFFF_FFFF, 0x003D_0B9B] {
            let encrypted = DiscoveryXMLCrypto.encrypt(original, offset: offset)
            let decrypted = DiscoveryXMLCrypto.decrypt(encrypted, offset: offset)
            #expect(decrypted == original, "round-trip failed for offset \(offset)")
        }
    }

    @Test("Empty payload is a no-op")
    func emptyIsNoOp() {
        let empty = Data()
        #expect(DiscoveryXMLCrypto.encrypt(empty, offset: 0xDEAD_BEEF) == empty)
        #expect(DiscoveryXMLCrypto.decrypt(empty, offset: 0) == empty)
    }

    @Test("Cipher with offset 0 is XOR with the unmodified key words")
    func zeroOffsetMatchesRawKey() {
        // With offset 0, the keystream is just the 8 u32 key
        // words flattened little-endian. XOR-ing zeros against
        // them surfaces the keystream itself — a useful pin
        // against accidental changes to the key constants.
        let zeros = Data(count: 32)
        let stream = DiscoveryXMLCrypto.encrypt(zeros, offset: 0)
        // First 4 bytes should be 0x1F2D3C4B in LE: 4B 3C 2D 1F.
        #expect(stream[0] == 0x4B)
        #expect(stream[1] == 0x3C)
        #expect(stream[2] == 0x2D)
        #expect(stream[3] == 0x1F)
        // Last 4 bytes (key word 7 = 0x17F2D3A5 LE = A5 D3 F2 17).
        #expect(stream[28] == 0xA5)
        #expect(stream[29] == 0xD3)
        #expect(stream[30] == 0xF2)
        #expect(stream[31] == 0x17)
    }

    @Test("Cipher cycles the 32-byte key when payload is longer")
    func cyclesKey() {
        // 64 bytes of zeros encrypted should produce the
        // keystream twice in a row.
        let zeros = Data(count: 64)
        let stream = DiscoveryXMLCrypto.encrypt(zeros, offset: 0)
        #expect(stream.prefix(32) == stream.suffix(32))
    }

    @Test("End-to-end: real captured success-reply bytes parse to registration")
    func endToEndCapturedReplyParses() throws {
        // Full 348-byte UDP payload of the first M2C_Q_R success
        // reply observed in `reolink-p2p.pcap`. The pipeline is:
        //   BcUdpPacket.decode → DiscoveryXMLCrypto.decrypt
        //   → DiscoveryXML.LookupResponse.decode
        // Anchors the entire wire stack against ground truth.
        let wire = Data(realWireReplyBytes)
        let (packet, consumed) = try #require(BcUdpPacket.decode(from: wire))
        #expect(consumed == wire.count)
        guard case .disc(let disc) = packet else {
            Issue.record("Expected .disc, got \(packet.kind)")
            return
        }
        let plaintext = DiscoveryXMLCrypto.decrypt(disc.payload, offset: disc.senderID)
        let parsed = try #require(DiscoveryXML.LookupResponse.decode(from: plaintext))
        #expect(parsed.rendezvous?.host == "172.232.163.180")
        #expect(parsed.rendezvous?.port == 58200)
        #expect(parsed.relay?.host == "172.232.163.180")
        #expect(parsed.relay?.port == 58101)
        #expect(parsed.responseCode == 0)
    }

